def _key(entry: tuple[int, str, str, int]) -> tuple[str, str]:
    return (entry[1].lower(), entry[2].upper())


def _merge_dedup(
    *lists: list[tuple[int, str, str, int]],
    sort_by_pop: bool = True,
) -> list[tuple[int, str, str, int]]:
    """Concatenate lists, dedup by (name, state), and re-rank from 1.

    sort_by_pop=True (default): re-sort by population desc.
    sort_by_pop=False: preserve the order in which entries were encountered
        across the input lists. Used by the "all" region to put Houston
        first, then the rest of Texas, then the rest of the US — instead of
        bumping NYC and LA to the front by population.
    """
    seen: set[tuple[str, str]] = set()
    merged: list[tuple[int, str, str, int]] = []
    for src in lists:
        for entry in src:
            k = _key(entry)
            if k in seen:
                continue
            seen.add(k)
            merged.append(entry)
    if sort_by_pop:
        merged.sort(key=lambda e: e[3], reverse=True)
    return [(i + 1, e[1], e[2], e[3]) for i, e in enumerate(merged)]


def top(n: int = 50, *, region: str = "us") -> list[tuple[int, str, str, int]]:
    """Return the top N cities for a region.

    region:
      "us"  — top US cities (default; ordered by population, ~1000 entries).
      "tx"  — top Texas cities (curated, ~100 entries down to ~14k pop).
      "hou" — Greater Houston metro (Houston + suburbs + notable CDPs).
      "all" — Houston metro first, then the rest of Texas, then the rest of
              the US. Within each tier, ordered by population. Deduped by
              (name, state). Use this when targeting Houston/Texas first.
    """
    region = region.lower()
    if region == "us":
        source = TOP_US_CITIES
    elif region == "tx":
        source = TOP_TX_CITIES
    elif region == "hou":
        source = _merge_dedup(GREATER_HOUSTON_CITIES)
    elif region == "all":
        # Tier order — Houston-metro → rest of TX → rest of US — is the
        # business-priority ordering, not population. Don't re-sort by pop.
        source = _merge_dedup(
            GREATER_HOUSTON_CITIES, TOP_TX_CITIES, TOP_US_CITIES,
            sort_by_pop=False,
        )
    else:
        raise ValueError(f"unknown region: {region!r} (expected 'us'|'tx'|'hou'|'all')")
    return source[: max(0, min(n, len(source)))]


def find(name: str, state: str | None = None) -> tuple[int, str, str, int] | None:
    """Look up a single city by name (case-insensitive)."""
    name_l = name.lower()
    state_u = state.upper() if state else None
    for entry in [*TOP_US_CITIES, *TOP_TX_CITIES, *GREATER_HOUSTON_CITIES]:
        if entry[1].lower() == name_l and (state_u is None or entry[2].upper() == state_u):
            return entry
    return None
